namespace Practice;

/*
    Названиями методов должны быть глаголы, а не существительные. Сущестивительные лучше для названий переменных и классов.
*/
public class Calculator
{
    public int Add(int a, int b)
    {
        return a + b;
    }

    public int Subtract(int a, int b)
    {
        return a - b;
    }

    public int Multiply(int a, int b)
    {
        return a * b;
    }

    public int Divide(int a, int b)
    {
        return a / b;
    }
}